// 菜单项工厂函数
export const createMenuItem = (
  id: string,
  label: string,
  iconName: keyof typeof Icons,
  onClick: () => void,
  options?: {
    color?: 'default' | 'warning' | 'danger';
    hidden?: boolean;
  }
): MenuItem => ({
  id,
  label,
  icon: React.createElement(Icons[iconName]),
  onClick,
  color: options?.color || 'default',
  hidden: options?.hidden || false,
});

export const createDivider = (): MenuItemType => ({
  type: 'divider',
});

